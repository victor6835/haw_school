// 如何使用數字，數字用法

var number = 6;
document.write((2+3)*number);
document.write("<br>")
document.write(8%3);/*8對3取餘數*/
document.write("<br>")
document.write(Math.abs(-6));/*取絕對值*/
document.write("<br>")
document.write(Math.max(2,3,4,5,15,46,number));/*回傳括號中最大的數*/
document.write("<br>")
document.write(Math.min(2,3,4,5,15,46,number));/*回傳括號中最小的數*/
document.write("<br>")
document.write(Math.round(4.5));/*回傳數的四捨五入*/
document.write("<br>")
document.write(Math.pow(4,3));/*回傳前數的(後數)次方*/
document.write("<br>")
document.write(Math.sqrt(64));/*回傳開根號值*/
document.write("<br>")
document.write(Math.round(Math.random()*10));/*回傳隨機數值*/
document.write('<br>')